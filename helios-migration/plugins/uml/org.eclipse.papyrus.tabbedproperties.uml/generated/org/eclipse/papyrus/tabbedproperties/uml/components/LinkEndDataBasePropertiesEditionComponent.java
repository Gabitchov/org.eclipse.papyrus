	public EObject getPropertiesEditionObject(EObject source) {
		if(source instanceof LinkEndData) {
			LinkEndData linkEndDataToUpdate = (LinkEndData)source;
			linkEndDataToUpdate.getOwnedComments().addAll(basePart.getOwnedCommentToAdd());
			linkEndDataToUpdate.getQualifiers().addAll(basePart.getQualifierToAdd());


			return linkEndDataToUpdate;
		} else
			return null;
	}

