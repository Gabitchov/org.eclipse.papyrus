	public EObject getPropertiesEditionObject(EObject source) {
		if(source instanceof Model) {
			Model modelToUpdate = (Model)source;
			modelToUpdate.setName(basePart.getName());

			modelToUpdate.setVisibility((VisibilityKind)basePart.getVisibility());

			modelToUpdate.getElementImports().addAll(basePart.getElementImportToAdd());
			modelToUpdate.getPackageImports().addAll(basePart.getPackageImportToAdd());
			modelToUpdate.getOwnedRules().addAll(basePart.getOwnedRuleToAdd());
			modelToUpdate.getTemplateBindings().addAll(basePart.getTemplateBindingToAdd());
			modelToUpdate.getPackageMerges().addAll(basePart.getPackageMergeToAdd());
			modelToUpdate.getPackagedElements().addAll(basePart.getPackagedElementToAdd());
			modelToUpdate.setViewpoint(basePart.getViewpoint());



			return modelToUpdate;
		} else
			return null;
	}

